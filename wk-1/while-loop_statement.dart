void main() {
  var list1 = [10, 20, 30, 40, 50]; // 5
  int i = 0;            // Initialize index

  while (i < list1.length) {  // Loop until i is less than the length of the list
    print(list1[i]);          // Print the current element at index i
    i++;                      // Increment the index after each iteration
  }
}//The while loop is used to execute a block of statements repeatedly until a given condition is true