void main() {
  // Example of a switch-case statement in Dart
  int month = 5;

  switch (month) {
    case 1:
      print("January: New beginnings!");
      break;

    case 2:
      print("February: Love is in the air!");
      break;

    case 3:
      print("March: Spring is coming!");
      break;

    case 4:
      print("April: Showers bring May flowers!");
      break;

    case 5:
      print("May: Blooming with possibilities!");
      break;
      
    default:
      print("Another month, another adventure!");
  }
}
// This code demonstrates the use of a switch-case statement to print messages based on the month number.